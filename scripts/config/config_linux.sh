build_linux_help()
{
cat << EOF
Linux kernel configurator for 486lin

Useage: $0 [help|-h|--help] | [clean|dist-clean|dist-clean-build] | [no-nuke] [LINUX_PATH=<PATH>] [LINUX_INSTALL_PATH=<PATH>]

Arguments:
clean: Clean up working directory and quit (default: Don't quit)

LINUX_PATH=<PATH>: PATH to the Linux source repo (required)
EOF
